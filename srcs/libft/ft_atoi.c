#include "libft.h"

int		ft_atoi(const char *s1)
{
	int nb;
	int i;

	nb = 0;
	i = 1;
	while (*s1 == ' ' || *s1 == '\n' || *s1 == '\t'
			|| *s1 == '\v' || *s1 == '\f' || *s1 == '\r')
		s1++;
	if (*s1 == '-')
		i = -1;
	if (*s1 == '+' || *s1 == '-')
		s1++;
	while (*s1 >= '0' && *s1 <= '9')
	{
		nb = nb * 10 + *s1 - '0';
		s1++;
	}
	return (nb * i);
}

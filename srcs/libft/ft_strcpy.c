#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char *ch;

	ch = dest;
	while (*src != '\0')
	{
		*ch = *(char*)src;
		ch++;
		src++;
	}
	*ch = '\0';
	return (dest);
}

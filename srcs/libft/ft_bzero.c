#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ch;
	int		i;

	i = 0;
	ch = (char*)s;
	while (n > 0)
	{
		ch[i] = 0;
		ch++;
		n--;
	}
}
